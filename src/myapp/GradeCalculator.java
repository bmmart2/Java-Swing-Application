    private String getLetterGrade(double in) {
        if (in < 80){
            if (in < 60)
                return "F";
            else if (in < 63)
                return "D-";
            else if (in < 67)
                return "D";
            else if (in < 70)
                return "D+";
            else if (in < 73)
                return "C-";
            else if (in < 77)
                return "C";
            else
                return "C+";
        }
        else {
            if (in < 83)
                return "B-";
            else if (in < 87)
                return "B";
            else if (in < 90)
                return "B+";
            else if (in < 93)
                return "A-";
            else if (in < 97)
                return "A";
            else
                return "A+";
        }
    }

