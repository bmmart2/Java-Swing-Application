    private void calcBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcBtnActionPerformed
        error.setText("");
        JLabel[] letters = {g1l, g2l, g3l, g4l, g5l, g6l};
        for (JLabel l : letters)
            l.setText("");
        try{
            JTextField[] grades = {g1, g2, g3, g4, g5, g6};
            JTextField[] weights = {g1w, g2w, g3w, g4w, g5w, g6w};
            
            int i = 0;
            double totalWeight = 0;
            double n = 0;
            double total = 0;
            for(JTextField f : grades){
                if (!f.getText().equals("")){
                    if(weights[i].getText().equals(""))
                        weights[i].setText("0");
                    total += Integer.parseInt(f.getText()) * Double.parseDouble(weights[i].getText());
                    totalWeight += Double.parseDouble(weights[i].getText());
                    letters[i].setText(getLetterGrade(Double.parseDouble(f.getText())));
                    
                }
                i++;
            }
            double avg = total/totalWeight;
            avg = Math.round(avg * 100);
            avg /= 100;
            average.setText(Double.toString(avg));
            letterGrade.setText(getLetterGrade(avg));
        } catch (NumberFormatException e) {
            error.setText("Error - One or more values are invalid.");
        }
    }//GEN-LAST:event_calcBtnActionPerformed

